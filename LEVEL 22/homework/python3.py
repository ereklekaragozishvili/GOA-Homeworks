# 4) გატესტეთ .upper(), .lower(), .capitalize(), .swapcase() და .find() მეთოდები



test_string = "Hello World"

print("Original:", test_string)
print("Upper:", test_string.upper())
print("Lower:", test_string.lower())
print("Capitalize:", test_string.capitalize())
print("Swapcase:", test_string.swapcase())
print("Find 'H':", test_string.find("H"))
print("Find 'World':", test_string.find("World"))
print("Find 'X':", test_string.find("X"))  
